def print_pitfalls(pf):
    """Print PITFALLS parameters."""
    
    print('   pf.l: %d'%(pf.l))
    print('   pf.r: %d'%(pf.r))
    print('   pf.s: %d'%(pf.s))
    print('   pf.n: %d'%(pf.n))
    print('   pf.d: %d'%(pf.d))
    print('   pf.p: %d'%(pf.p))
    print('   pf.rem_cycle: %d'%(pf.rem_cycle))
    print('   pf.dist: %s'%(pf.dist))

