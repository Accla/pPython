def print_falls(f):
    """Print FALLS parameters."""
    
    print('   f.l: %s'%(f.l))
    print('   f.r: %s'%(f.r))
    print('   f.s: %s'%(f.s))
    print('   f.n: %s'%(f.n))
    print('   f.local_len: %s'%(f.local_len))
    print('   f.complete_cycle: %s'%(f.complete_cycle))
    print('   f.complete_block: %s'%(f.complete_block))

